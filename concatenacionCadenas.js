// Funciones Incorporadas en JavaScript


// Concatenacion de Cadenas
let cadena1 = 'Hola', cadena2 = 'Mundo'
let cadena3 = cadena1 + ' ' + cadena2
console.log(cadena3)
// String Interpolation
let cadena4 = `${cadena1} ${cadena2}`
console.log(cadena4)

// Si son numeros se suman
let a = 5, b = 3
console.log(a + b)

// Si son cadenas se concatenan
let c = '5', d = '3'
console.log(c + d)