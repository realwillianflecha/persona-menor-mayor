// Operadores Logicos
let a = true
let b = false
console.log(a)
console.log(b)

// Operador Logico && (and o Y)
// Regresa verdadero si ambos operandos
// son verdaderos
console.log(`${a} && ${b} -> ${a && b}`)

// Operador Logico || (or o O)
// Regresa verdadero si cualquiera de los operandos
// es verdadero
console.log(`${a} || ${b} -> ${a || b}`)

// Operador ! (not o NO)
console.log(`!${a} -> ${!a}`)