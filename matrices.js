// Matrices en JavaScript
// Declaracion de una Matriz
let matriz = [[],[]]
// Modificar los valores de una matriz
matriz[0][0] = 100
matriz[0][1] = 200
matriz[0][2] = 300
matriz[1][0] = 400
matriz[1][1] = 500
matriz[1][2] = 600
// Accedemos a los valores
console.log(`Valor [0][0] = ${matriz[0][0]}`)
console.log(`Valor [1][2] = ${matriz[1][2]}`)
