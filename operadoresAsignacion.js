// Operadores de Asignacion en JavaScript
// Para asignar un valor =
let miNumero = 10
console.log(miNumero)

miNumero = 20
console.log(miNumero)

// Operador +=
miNumero += 5 // miNumero = miNumero + 5
console.log(miNumero)

// Operador -=
miNumero -= 3
console.log(miNumero)

// Operador *=
miNumero *= 2
console.log(miNumero)

// Operador /=
miNumero /= 4
console.log(miNumero)

// Operador %=
miNumero %= 2
console.log(miNumero)

//Math.pow(2, 3)
miNumero = 2 ** 3
console.log(miNumero) // 2*2*2 = 8

// Operador **=
miNumero **= 2
console.log(miNumero)